using UnityEngine;
using TMPro;
using System.Collections;

public class CountdownScript : MonoBehaviour
{
    [Header("GameOverPanel")]
    public GameObject gameOverScreen; // ?????? ?? ?????? GameOverMenu

    [Header("CountdownParameters")]
    public TextMeshProUGUI countdownText; // ?????? ?? TextMeshPro ??? ??????????? ????????? ???????
    public int timer = 60; // ????? ????????? ???????

    private void Start()
    {
        StartTimer();
    }

    void StartTimer()
    {
        StartCoroutine(Timer());
    }

    IEnumerator Timer()
    {
        while (timer > 0)
        {
            countdownText.text = timer.ToString();
            yield return new WaitForSeconds(1f);
            timer--;
        }

        // ???????? ?????? ????????, ??????? GameOverMenu ???????
        gameOverScreen.SetActive(true);
    }
}
